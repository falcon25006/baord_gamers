          Le XXe siècle et l'ère moderne:
        </h2>
          
          <p>
            Le XXe siècle a été marqué par des figures emblématiques comme José Raúl Capablanca et Bobby Fischer. Capablanca, avec son style 
            fluide et son approche stratégique, a dominé le jeu pendant les années 1920 et 1930. Fischer, quant à lui, a révolutionné les
             échecs dans les années 1960 et 1970 avec ses victoires contre les meilleurs joueurs soviétiques et sa conquête du titre de 
             champion du monde en 1972. Sa victoire contre Boris Spassky pendant la Guerre Froide est l'un des moments les plus célèbres
              de l'histoire des échecs.
          
         <h2> L'impact de l'informatique:</h2><br>
          
          À la fin des années 1990, l'impact des ordinateurs sur les échecs a été significatif. Deep Blue, un ordinateur développé par
           IBM, a battu Garry Kasparov, alors champion du monde, en 1997. Cet événement a marqué le début d'une nouvelle ère où les 
           ordinateurs sont devenus des outils essentiels pour l'analyse des parties et l'amélioration des stratégies.
          </p>
         <h2>
          Le XXIe siècle et l'avenir:
         </h2>
          
         <p>
          Aujourd'hui, les échecs sont un sport mondial avec des millions de joueurs et des compétitions à tous les niveaux. Le championnat du monde est dominé par Magnus Carlsen, qui a été couronné en 2013. Les plateformes en ligne, comme Chess.com et Lichess, ont démocratisé le jeu et permettent aux joueurs de tous niveaux de participer à des parties et à des tournois du monde entier.
          L'émergence de l'intelligence artificielle, avec des outils comme Stockfish et AlphaZero, continue de repousser les limites de la compréhension du jeu, offrant de nouvelles perspectives et techniques aux joueurs et aux analystes.
          Ainsi, les échecs ont évolué d'un ancien jeu militaire en Inde à un sport mondial sophistiqué, intégrant des innovations technologiques tout en conservant ses éléments fondamentaux.
          La réforme de la reine, qui a transformé ses mouvements et accru considérablement sa puissance, a été un processus complexe et graduel, plutôt qu'une initiative d'un seul individu. Cependant, plusieurs éléments clés et figures historiques ont contribué à cette transformation majeure des règles du jeu d'échecs.
         </p>
